    @Override
    public double masaAngsuran(){
        MainJumlah angsur = new MainJumlah();

        angsur.setAjuan(Integer.parseInt(inputAjuan.getText()));
        angsur.setJangka(Integer.parseInt(inputJangka.getText()));
        if (angsuranHarian.isSelected()){
            angsur.setMasa(30);
        }else if (angsuranMingguan.isSelected()){
            angsur.setMasa(4);
        }else if (angsuranBulanan.isSelected()){
            angsur.setMasa(1);
        }
        angsur.setPokok(angsur.getAjuan()/angsur.getJangka()/angsur.getMasa());
        angsur.setBunga(angsur.getAjuan() * 0.5/100 /angsur.getMasa());
        
        double a;
        a = angsur.getPokok() + angsur.getbunga();
        
        return a;
    }
    
    @Override
    public double jumlahAngsuran(){
        MainJumlah angsur = new MainJumlah();

        angsur.setAjuan(Integer.parseInt(inputAjuan.getText()));
        angsur.setJangka(Integer.parseInt(inputJangka.getText()));
        if (angsuranHarian.isSelected()){
            angsur.setMasa(30);
        }else if (angsuranMingguan.isSelected()){
            angsur.setMasa(4);
        }else if (angsuranBulanan.isSelected()){
            angsur.setMasa(1);
        }
        angsur.setPokok(angsur.getAjuan() / angsur.getJangka() / angsur.getMasa());
        angsur.setBunga(angsur.getAjuan() * 0.5/100 /angsur.getMasa());
        angsur.setAngs(angsur.getPokok() + angsur.getbunga());
        
        double b;
        b = angsur.getAngs()*angsur.getMasa()*angsur.getJangka();
        
        return b;
    }
