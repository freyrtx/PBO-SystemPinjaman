    private void totalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_totalActionPerformed
        // TODO add your handling code here:
        int inkre = pilihKredit.getSelectedIndex();
        int ajuan = Integer.parseInt(inputAjuan.getText());
        
        switch (inkre){
            case 0: {
                try{
                    if(ajuan>25000000){
                        throw new ArrayIndexOutOfBoundsException();
                    }else{
                        masaAngsuran.setText(String.format("%.1f",masaAngsuran()));
                        totalAngsuran.setText(String.format("%.1f",jumlahAngsuran()));
                        break;
                    }
                }catch(Exception e){
                    JOptionPane.showMessageDialog(this,"Batas Maksimal KUR Mikro adalah Rp. 25.000.000");
                    break;
                }
            }
            case 1: {
                try{
                    if(ajuan<25000000 || ajuan>100000000){
                        throw new ArrayIndexOutOfBoundsException();
                    }else{
                        masaAngsuran.setText(String.format("%.1f",masaAngsuran()));
                        totalAngsuran.setText(String.format("%.1f",jumlahAngsuran()));
                        break;
                    }
                }catch(Exception e){
                    JOptionPane.showMessageDialog(this,"Batas peminjaman KUR Ritel adalah Rp. 25.000.000 - Rp. 100.000.000");
                    break;
                }
            }
            case 2: {
                try{
                    if(ajuan>25000000){
                        throw new ArrayIndexOutOfBoundsException();
                    }else{
                        masaAngsuran.setText(String.format("%.1f",masaAngsuran()));
                        totalAngsuran.setText(String.format("%.1f",jumlahAngsuran()));
                        break;
                    }
                }catch(Exception e){
                    JOptionPane.showMessageDialog(this,"KUR TKI tidak boleh lebih dari Rp. 25.000.000");
                    break;
                }
            }
        }
        
    }//GEN-LAST:event_totalActionPerformed

